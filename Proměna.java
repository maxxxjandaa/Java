public class Proměna {
    public static void main(String[] args) {

        //numeric values
        {
            byte a = 1; //-128 - 127
            short b = 1; //-32 768 - 32 767
            int c = 1;  // hodně
            long d = 1; // hodně moc

        }
        //numeric values with a decimal point
        {
            float e = 1.0f; //23 b
            double f = 1.0d; //52 b
        }

        //true/false
        {
            boolean g = true;
        }
        //text/letters
        {
            char i = 'a';
            String j = "abc";
        }
    }
}
